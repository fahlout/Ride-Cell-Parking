import UIKit

extension UIColor {
    class var rideCellBlue: UIColor {
        get {
            return UIColor(red: 107.0/255.0, green: 191.0/255.0, blue: 207.0/255.0, alpha: 1.0)
        }
    }
    
    class var rideCellRed: UIColor {
        get {
            return UIColor(red: 195.0/255.0, green: 54.0/255.0, blue: 89.0/255.0, alpha: 1.0)
        }
    }
}
